function switchDimension(dimension) {
    // Si se hace clic en la misma dimensión activa → deseleccionar (restauras sin cambiar dataset base)
    if (state.dimension === dimension) {
        document.querySelectorAll('.dimension-btn').forEach(btn => {
            btn.classList.remove('active', 'bg-blue-600');
            btn.classList.add('bg-gray-700', 'hover:bg-gray-600');
        });
        // Mantengo dimension y categoría vigentes para no romper pipelines (si quieres "reset total", aquí podrías fijar defaults)
        renderCategoryButtons();
        renderTTIcons();
        updateTotals();
        renderCharts();
        return;
    }

    // Caso normal: cambiar a una nueva dimensión
    state.dimension = dimension;

    // Actualizar botones de dimensión
    document.querySelectorAll('.dimension-btn').forEach(btn => {
        btn.classList.remove('active', 'bg-blue-600');
        btn.classList.add('bg-gray-700', 'hover:bg-gray-600');
    });

    const activeBtn = document.getElementById(
        dimension === 'orientation' ? 'dimOrientation' : 'dimType'
    );
    activeBtn.classList.add('active');
    activeBtn.classList.remove('bg-gray-700', 'hover:bg-gray-600');

    // Cambiar a la primera categoría de la nueva dimensión
    state.activeCategory = Object.keys(classifications[dimension])[0];

    // Renderizar interfaz y datos actualizados
    renderCategoryButtons();
    renderTTIcons();
    updateTotals();
    renderCharts();
}

