function updateTotals() {
    const activeTTs = classifications[state.dimension][state.activeCategory];
    const total = data.thinkTanks
        .filter(tt => activeTTs.includes(tt.name))
        .reduce((sum, tt) => sum + tt.total, 0);
    
    document.getElementById('totalCount').textContent = total.toLocaleString();
}

function setupEventListeners() {
    // Control de reproducción
    document.getElementById('playBtn').addEventListener('click', togglePlay);
    document.getElementById('prevBtn').addEventListener('click', () => navigateCategory(-1));
    document.getElementById('nextBtn').addEventListener('click', () => navigateCategory(1));
    
    // Control de dimensión (tu toggle ya probado)
    document.getElementById('dimOrientation').addEventListener('click', () => switchDimension('orientation'));
    document.getElementById('dimType').addEventListener('click', () => switchDimension('type'));
    
    // Control de categorías
    document.getElementById('categoryFilters').addEventListener('click', (e) => {
        if (e.target.classList.contains('category-btn')) {
            state.activeCategory = e.target.dataset.category;
            renderCategoryButtons();
            renderTTIcons();
            updateTotals();
            renderCharts();
            animateTotalCapsule();
        }
    });
}

function togglePlay() {
    state.isPlaying = !state.isPlaying;
    const playBtn = document.getElementById('playBtn');
    const icon = playBtn.querySelector('svg');
    
    if (state.isPlaying) {
        icon.setAttribute('data-feather', 'pause');
        startAutoPlay();
    } else {
        icon.setAttribute('data-feather', 'play');
    }
    feather.replace();
}

function startAutoPlay() {
    if (!state.isPlaying) return;
    
    const categories = Object.keys(classifications[state.dimension]);
    const currentIndex = categories.indexOf(state.activeCategory);
    const nextIndex = (currentIndex + 1) % categories.length;
    
    state.activeCategory = categories[nextIndex];
    renderCategoryButtons();
    renderTTIcons();
    updateTotals();
    renderCharts();
    
    setTimeout(() => startAutoPlay(), 2000);
}

function navigateCategory(direction) {
    const categories = Object.keys(classifications[state.dimension]);
    const currentIndex = categories.indexOf(state.activeCategory);
    const nextIndex = (currentIndex + direction + categories.length) % categories.length;
    
    state.activeCategory = categories[nextIndex];
    renderCategoryButtons();
    renderTTIcons();
    updateTotals();
    renderCharts();
}

