function ensureTooltip() {
    let tooltip = document.getElementById('tooltip');
    if (!tooltip) {
        tooltip = document.createElement('div');
        tooltip.id = 'tooltip';
        tooltip.style.position = 'absolute';
        tooltip.style.pointerEvents = 'none';
        tooltip.style.background = 'rgba(0,0,0,0.85)';
        tooltip.style.color = '#fff';
        tooltip.style.padding = '8px 12px';
        tooltip.style.borderRadius = '8px';
        tooltip.style.fontSize = '12px';
        tooltip.style.border = '1px solid #EE5D49'; // acento naranja
        tooltip.style.backdropFilter = 'blur(6px)';
        tooltip.style.display = 'none';
        tooltip.style.zIndex = '9999';
        document.body.appendChild(tooltip);
    }
    return tooltip;
}

// devuelve el TT por clave (usa data.thinkTanks global)
function getThinkTankByKey(key) {
    return data.thinkTanks.find(tt => tt.id === key || tt.name === key);
}

function hideTooltip() {
    const tooltip = document.getElementById('tooltip');
    if (tooltip) tooltip.style.display = 'none';
}

