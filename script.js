// ===== Tooltips específicos =====
function handleBarHover(event, d) {
    const tooltip = ensureTooltip();

    // clave del think tank (grupo padre del rect)
    const key = this.parentNode.__data__.key;
    const ttInfo = getThinkTankByKey(key);
    const logo = ttInfo?.logoUrl || '';
    const name = ttInfo?.name || key;

    const fecha = d3.timeFormat('%B %Y')(new Date(d.data.date + '-01'));
    const valor = Math.round(d[1] - d[0]);

    tooltip.innerHTML = `
        <div style="display:flex;align-items:center;">
            <img src="${logo}" alt="${name}" 
                 style="width:24px;height:24px;border-radius:50%;margin-right:8px;border:1px solid #666;">
            <div>
                <div style="font-weight:600;">${name}</div>
                <div style="opacity:0.8;">${fecha}</div>
                <div style="opacity:0.8;">Valor: ${valor}</div>
            </div>
        </div>
    `;
    const rect = document.getElementById('centralChart').getBoundingClientRect();
    tooltip.style.left = (event.clientX - rect.left + 8) + 'px';
    tooltip.style.top  = (event.clientY - rect.top  + 8) + 'px';
    tooltip.classList.remove('hidden');
    tooltip.style.left = (event.pageX + 15) + 'px';
    tooltip.style.top  = (event.pageY - 40) + 'px';
    tooltip.style.display = 'flex';
}

function handleBottomBarHover(event, d) {
    const tooltip = ensureTooltip();
    const dateStr = d.data.date;
    const dateFmt = d3.timeFormat('%B %Y')(new Date(dateStr + '-01'));
    const value = Math.round(d[1] - d[0]);
    const categoryKey = d3.select(event.target.parentNode).datum().key;

    tooltip.innerHTML = `
        <div style="font-weight:600;margin-bottom:4px;">${dateFmt}</div>
        <div>Categoría: ${categoryKey}</div>
        <div>Valor: ${value.toLocaleString()}</div>
    `;
    tooltip.style.left = (event.pageX + 15) + 'px';
    tooltip.style.top  = (event.pageY - 45) + 'px';
    tooltip.style.display = 'block';
}
